public class OperatorJava {
    public static void main(String args[]) {
        int a = 8;
        int b = 3;
        int hasil1,hasil2,hasil3,hasil4,hasil5,hasil6;
        
        hasil1 = a & b;
        hasil2 = a | b;
        hasil3 = a ^ b;
        hasil4 = a << b;
        hasil5 = a >> b;
        hasil6 = -a;
        
       
        
      System.out.println("Hasil 1 : "+hasil1);
      System.out.println("Hasil 2 : "+hasil2);
      System.out.println("Hasil 3 : "+hasil3);
      System.out.println("Hasil 4 : "+hasil4);
      System.out.println("Hasil 5 :"+hasil5);
      System.out.println("Hasil 6 : "+hasil6);
      
             
      
        
    }
}
